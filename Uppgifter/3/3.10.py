"""
Övning 3.10 - Dictionary
I en dictionary skapar man par bestående av data och en nyckel. Data-accesser sker genom nyckeln. Man kan se en dictionary som en avancerad lista, 
där man istället för index-nummer kan använda olika typer av data. Skriv följande i Python-terminalen:
 
>>> numbers = {'one': 1, 'two': 2, 'seven': 6, 'eight': 9}
>>> numbers[’one’]
1
 
För att vänja dig vid att hantera dictionaries kan du därefter skriva kommandon för att göra följande:
lägg till nyckeln 'three' och koppla ihop den med ett lämpligt tal
ändra i din dictionary numbers så att numbers['eight'] kopplas ihop med talet 8 istället.
"""
numbers = {"one": 1, "two": 2, "seven": 6, "eight": 9}
print(numbers["one"])
